namespace Modules\Financeiro\Enuns;


use App\Enuns\BaseEnum;
use App\Traits\Hydrator;
use App\Traits\ToArray;

class TipoLancamento extends BaseEnum
{
	use ToArray, Hydrator;

	public const ENTRADA = 0;
	public const SAIDA = 1;

	private $id;
	private $descricao;

	public function __construct($modulo = null)
	{
		if (!is_null($modulo)) {
			$this->hydrate($modulo);
		}
	}

	protected static $typeLabels = [
		self::ENTRADA => [self::ENTRADA, "Contas à Receber"],
		self::SAIDA => [self::SAIDA, "Contas à Pagar"]
	];

	public static function labels()
	{
		return array_map(function ($item) {
			return [
				'id' => $item[0],
				'descricao' => $item[1],
			];
		}, static::$typeLabels);
	}

}
